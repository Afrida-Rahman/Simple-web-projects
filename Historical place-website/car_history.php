		<p>Aboriginal people inhabited the Sydney basin for thousands of years prior to the arrival of Europeans. The northern coastal area of Sydney was home to the Guringai people, western Sydney was home to the Dharug clans, and southern Sydney was inhabited by the Dharawal clans. The Guringai lived primarily along the foreshores of the harbour, and fished and hunted in the waters and hinterlands of the area. All clans harvested food from their surrounding bush. Self-sufficient and harmonious, they had no need to travel far from their lands, since the resources around them were so abundant, and trade with other tribal groups was well established. The British arrival in 1788 had a dramatic impact on all of the Sydney clans. Food resources were quickly diminished by the invaders, who had little understanding of the local environment. As a result, the Aboriginal people throughout the Sydney Basin were soon close to starvation. The Sydney clans fought back against the invaders, but the introduction of diseases from Europe and Asia, most notably smallpox, destroyed over half the population. The clearing of land for settlements and farms displaced local tribes and reduced the availability of natural food resources, leaving Aboriginal people reliant on white food and clothing. The French surgeon and pharmacist Rene Primavere Lesson, who visited Sydney in 1824, wrote: "the tribes today are reduced to fragments scattered all around Port Jackson, on the land where their ancestors lived and which they do not wish to leave". (Information taken from City of Ryde Aboriginal Site Management Report, Aboriginal Heritage Office, 2011) </p>
		<p>The suburb known today as Marsfield was formed from the Field of Mars Common, an area of 5050 acres (2044 hectares) declared in 1804 by Governor King as one of six ‘commons’ in the Colony, to be used for the grazing of livestock by local residents. The Common followed the path of the Lane Cove River and consisted mainly of heavily timbered bush. By the 1840s the area was notorious as the haunt of unsavory society, home to a range of activities such as gambling, sly grog selling and a range of antisocial activities. Following many years of community agitation and debate including a Parliamentary Select Committee, by 1874 the Common was formally resumed to allow sale and settlement by small farmers. The money raised was to be used to fund the crossings of the Parramatta River at Gladesville and Iron Cove, both of which were being demanded by the residents of the Ryde area. </p>
		<p>A regular grid pattern of streets was laid out and the land subdivided into small farms of between 0.4 and 1.6ha, with areas reserved for open space and special purposes. The street names chosen continued the martial tradition of the "Field of Mars" and referenced famous battles and British victories. The land itself was released for sale in stages, with the first being in the east Ryde area in 1885. A considerable proportion of purchasers bought multiple lots, paving the way for later re-subdivision. </p>
