<h1>Blast of Paddy</h1>
<h3>DISEASE: Blast of Paddy<br>
PATHOGEN: Pyricularia grisea (
Magnaporthe grisea
)<br>
HOSTS: Paddy</h3></center>
<img src="images/blast_paddy.jpg" width="400" height="300"style="float : right"/>

<h2>Symptoms and Signs</h2>
<p>Blast of Paddy: Blast symptoms can occur on leaves,leaf
collars, nodes and panicles. <br>* Leaf blast, the lesions are
elliptical or spindle shaped with brown borders and gray
centers. <br>* Collar blast or node blast occurs when the pathogen
infects the collar that can ultimately kill the entire leaf
blade. <br>* Neck blast occurs when the pathogen infects the neck
of the panicle. <br>* The infected neck is girdled by a grayish brown
lesion and the panicle falls over if the infection is severe. <br>* If
neck blast occurs before the milk stage, the entire panicle may
die prematurely, leaving it white and completely unfilled. <br>* The
fungus produces two toxins-µ-picolinic acid and piricularin
Microscopically pyriform (pear‐shaped) conidia are usually
three‐celled or 2 septate borne on conidiophores</p>
<h2>Predisposing Factors ‐ Rice Blast</h2>
<p>
1.Upland conditions<br>
2.Extended dew periods<br>
3.High relative humidity<br>
4.intermittent rain showers or drizzle<br>
5.High nitrogen fertilizer<br>
</p>
<h2>Management of Blast of Paddy</h2>
<p>• Use of resistant cultivars.<br>
• Efficient use of nitrogen fertilizers.<br>
• Soil amendment with silica.<br>
• Spray Beam (tricyclazole) –melanin
biosynthesis inhibitor fungicide for
effective control
</p>

