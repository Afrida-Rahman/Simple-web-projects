<h2>Disease symptoms:</h2>
<p>*  In addition to the potato, the pathogen also damages plants such as chili, tomato, tobacco and egg plant, as well as several species of weeds.
<br>*  The symptoms of bacterial wilt infection can be seen on all parts of infected plants.
<br>*  Infected plant begins to wilt, starting from the tips of the leaves or where the stems branch out, and then spreading to all parts of the plant.
<br>*  Leaves become yellow at their bases, then the whole plant wilts and dies. When stems are cut a brown colored ring will be visible.
<br>*  When a tuber is cut in half, black or brown rings will, however, be visible. If left for a while or squeezed, these rings will exude a thick white fluid.
<br>*  A further symptom is fluid coming out of tuber eyes. This can be signified by soil sticking to tuber eyes when crops are harvested. Serious infection causes tubers to rot.</p>
<h2>Survival and spread:</h2>
<p>Bacterial wilt pathogen can survive in soil (without a host for several seasons), water, seed tubers, potato plant remnants.
The disease can spread from field to field or from plant to plant within field via infected seed, air, water, soil, farming tools, livestock and people.</p>
<h2>Favourable conditions:</h2>
<p>*  High temperature, soil moisture, low pH.
<br>*  The disease spread rapidly in the warmer temperatures in storage areas. <br>*  Infected seed can also be a source of the disease in the field.</p>
<h2>Minimising the occurrence</h2>
<p>Bacterial wilt is difficult to control (or eradicate) because of the soil-borne nature of its causal organism. Therefore, following options should be considered in managing the disease.


<br>*  Adopt rotations with pastures, cereals and non-solanaceous crops for periods exceeding five years.<br>
*  Use of certified seed from reliable sources. Exclusion of the disease may be exercised by quarantine or other legislative measures. For example, Tasmania, which so far has not recorded bacterial wilt, is very careful to import only healthy seed. New Zealand and South Africa ban the importation of seed from areas known to have the disease. Other measures of control include:
<br>*  Planting in areas where bacterial wilt has not occurred previously.<br>
*  Control self-sown potatoes.<br>
*  Control weed hosts such as nightshade, thorn apple, Narrawa burr around dames, along channels and in the paddocks after cropping potatoes.
<br>*  Avoid deep ploughing â€“ the organisms survive in the deep, cool layers of soil.<br>
*  Irrigation water should never be allowed to run freely over or below the soil surface. It should never be allowed to return to the dam or stream from which it is pumped, nor to any other irrigation source.
<br>*  Regular crop inspection for disease symptoms and remove and destroy diseased plants, tubers and immediate neighbours.<br>
*  Use stock to clean up chats, discarded tubers and crop debris, but do not allow the stock back onto clean paddocks.<br>
*  Do not return potato waste, e.g. oversized, misshapen and diseased tubers to paddocks.<br>
<h2>Minimising the spread</h2>
<p>
*  Machinery taken onto a diseased paddock should be left on the paddock while it is being worked.<br>
*  Machinery removed from the paddock should then be washed clean with a disinfectant solution in a dedicated area for equipment wash down.
<br>*  Use high-pressure wash to clean machinery, sheds etc to remove soil adhering to any surfaces.<br>
*  Clothing and boots of people working in the paddock should be exchanged for clean items when leaving the paddock, or else boots should be washed in a suitable disinfectant.
<br>*  After harvest, all diseased and discarded tubers should be collected and buried at least one metre underground.<br>
*  On no account should any of the produce from a diseased crop be kept as seed.<br>
*  Load and unload vehicles only in designated areas with sealed or hard ground or bare paddocks away from potato paddocks.<br>
*  Choose transport roots that minimise travel through potato paddocks and regions.<br>
*  If second-hand bags or half tonne bins have been used to hold potatoes, these should be thoroughly washed and disinfected before being used again. Bags should be disinfected or discarded.
<br>*  Ask visitors, contractors and workers to wear overalls, gumboots and overshoes on the property.</p>
