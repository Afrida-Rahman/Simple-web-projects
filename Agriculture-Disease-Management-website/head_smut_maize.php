<h2>Symptoms and Signs</h2>
<p>Characteristic symptoms
of Head Smut of
Maize-ears and tassels
may be replaced with
smut sori filled with
teliospores</p>
<h2>Epidemiology</h2>
<p>The fungus survives as thick-walled
black teliospores in the soil produced
by the head smut fungus. Plants are
systemically infected as seedlings and
head smut develops systemically into
the ear and tassel (invades the
meristematic tissue).</p>
<h2>Disease management</h2>
<p>The disease is soil borne. But seed can also carry
teliospores.
<h3>• Chemical</h3>
<p>Seed treatment : Systemic fungicides like
Carboxin, Vitavax and Benlate @ 2.0 g/Kg
seed and Tilt (propioconazole ) 25EC @ 0.1%<p>
<h3>• Cultural.</h3><p>
Rogue out –removal of smutted heads.</p>
<h3>• Resistance.</h3> <p>Use resistant cultivars or at least
avoid very susceptible cultivars.</p>

