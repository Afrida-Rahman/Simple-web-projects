<h1>Northern maize leaf blight or
Turcicum leaf blight of Maize</h1>
<h3>Disease: Northern leaf blight or
Turcicum leaf blight of Maize<br>
Pathogen:<br>
Anamorph (asexual phase)<br>
• Exserohilum turcicum or (syn.
Helminthosporium turcicum)<br>
Teliomorph (sexual phase)<br>
• Setosphaeria turcica<br>
Host: Maize</h3></center>
<img src="images/leaf_blight_Maize.jpg" width="400" height="300"style="float : right"/>

<h2>Symptoms</h2>
<p>Lesions are elliptical and tan in color, developing
distinct dark areas as the fungus sporulates. <br>Lesions
typically first appear on lower leaves and spread
upwards.<br>Under severe infection, lesions may coalesce,
blighting the entire leaf. </p>
<h2>Predisposing Factors</h2>
<p>•Less warm and wet
conditions<br>
•Monoculture<br>
•Reduced tillage</p>
<h2>Disease management</h2>
<p>• The use of resistant varieties.<br>
• General sanitation-Destruction of
crop debris.<br>
• Rotation with non-host species.<br>
• Spray Zineb/Maneb @ 0.25% at 10
days intervals</p>
