<h1>Sheath blight of rice</h1>
<h3>DISEASE: Sheath blight
of rice<br>
PATHOGEN:
Rhizoctonia solani
Kuhn<br>
HOSTS: Paddy</h3></center><br>
<img src="images/sheath_blight_paddy.jpg" width="400" height="350"style="float : right"/>

<h2>Symptoms and Signs</h2>
<p>Initial lesions are
small, ellipsoidal or
ovoid, greenishgray
and watersoaked
and usually
develop near the
water line in lower
leaves.<br>**Older lesions are elliptical or ovoid with a grayish
white center and light brown to dark brown margin.
Lesions may coalesce forming bigger lesions with
irregular outline and may cause the death of the whole
leaf.<br>**Severely infected plants produced poorly filled
or empty grains, especially those on the lower
portion of the panicles. Brown sclerotia of the
fungi are observed on the infected area of the
plant.<br>**Sclerotia on the soil or floating on the
water are assumed to be the sole source
of inoculum in temperate regions. </p>
<h2>Epidemiology</h2>
<p>• Microclimatic conditions Warm (28-32
oC) –<br>
humid (RH-95%) with low sunlight favor the
disease.<br>
• Disease development - in the early heading and
grain filling growth stage<br>
• Soil borne sclerotia, and to a lesser degree
mycelium in plant debris, are means of pathogen
survival between crops and are the primary
inoculum.<br>
• Sclerotia floating in the paddy field and infection
occur near the water line.</p>
<h2>Management of Sheath blight of rice</h2>
<h3>Host resistance:<br></h3><p>
• No resistant cultivars.<br>
<h3>Cultural practices:</h3><p>
• General sanitation-destruction of crop debris.<br>
• Flooding of rice fields.<br>
• Proper spacing.<br>
• Efficient use of N-fertilizers.<br></p>
<h3>Chemical control:</h3><p>
• Spray of Tilt (Propioconazole) ,<br>a combination of fungicides
(Azoxystrobin 18.2% + Difenoconazole 11.4% SC) was found
highly effective against rice sheath blight.</p>

