<h2>History of Karnal bunt</h2>
<p>Karnal bunt is so named because it
was discovered in 1931 on wheat
grown near Karnal, India by Mitra.
Since then, it has been found in all
major wheat-growing states of
India, as well as in Pakistan, Iraq,
Mexico, and Afghanistan. </p>
<h2>Symptoms and Signs</h2>
<p>Karnal bunt is difficult to
identify in the field.
Developing wheat kernels
are randomly infected and
usually only partially
converted to the fungus
and hence, called partial
bunt.<br>**The disease is not usually noticed until
the grain is threshed and partially
smutted kernels are exposed. Generally
a few florets per spike are affected. <br>**While diseased seeds usually retain a
partial seed coat, the embryo and part of the
endosperm have been converted to masses
of small black spores, which emit a fishy
odor trimethylamine)<br>**Teliospores are dark brown to black in colour.
Ellipsoidal to spherical in shape.
Characteristic apiculus and episore is reticulate with curved spines.</p>
<h2>Management of Karnal bunt of Wheat</h2>
<p>Disease control:
The disease is basically seed and soil borne.<br>
• Use of resistant varieties:<br>
The most successful method is the use of resistant
varieties.<br>
• Crop rotation:<br>
Crop rotation at suitable intervals is also effective in
disease control.<br>
• Use disease free Seed<br>
• Spray of Tilt (propioconazole) 25EC @ 0.1% at
heading (anthesis) stage. 82
83<br>
<h3>Seed treatment</h3><p>
(a) Hot water treatment:<br>
The seeds are first soaked in water for five hours at 20˚C , the
water is drained off and then they are treated with hot water at
49˚C for about a minute and finally with hot water at 52˚C for
11 minutes. Immediately after the hot water treatment , the
seeds are cooled off by dipping in cold water and dried. The
dormant mycelium inside the seed dies off by this treatment</p>
<p>(b) Use of systemic fungicides:<br>
Fungicides like carboxin, vitavax , and benlate @ 2.0 g/Kg
seed and propioconazole @ 0.1% are used for seed treatment
to reduce the pathogen infectivity. A combination of vitavax
with thiram is very effective for disease control<br></p>
