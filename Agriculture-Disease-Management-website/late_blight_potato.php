<h3>
<center>
DISEASE:Late Blight<br>
PATHOGEN: Phytophthora infestans<br>
HOST:Potato
</center>
</h3><br>
<img src="images/late_blight_potato.jpg" width="400" height="300"style="float : right"/>

<p>Found on tomato and potato plants, late blight is caused by the fungus Phytophthora infestans and is common throughout the United States. True to its name, the disease occurs later in the growing season with symptoms often not appearing until after blossom.</p>
<p>Late blight first appears on the lower, older leaves as water-soaked, gray-green spots. As the disease matures, these spots darken and a white fungal growth forms on the undersides. Eventually the entire plant will become infected. Crops can be severely damaged.</p>

<p>Unlike other fungal diseases, this plant problem does not overwinter in the soil or on garden trash. Instead the spores are introduced by infected tubers, transplants or seeds. Wind will also carry the disease from nearby gardens. Warm temperatures (70-80˚F) and wet, humid conditions promote its rapid spread.</p>
Note: Late Blight was responsible for the Irish potato famine (1845-1849).
<h2>Treatment</h2>
<p>*  Plant resistant cultivars when available.<br>
*  Remove volunteers from the garden prior to planting and space plants far enough apart to allow for plenty of air circulation.<br>
*  Water in the early morning hours, or use soaker hoses, to give plants time to dry out during the day — avoid overhead irrigation.<br>
*  Destroy all tomato and potato debris after harvest (see Fall Garden Cleanup).<br><br>
<h3>If symptoms are observed, treat plants with one of the following fungicides:</h3><p>

