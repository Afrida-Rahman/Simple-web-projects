  <b style=font-size:50px>A</b>griculture is the cultivation of land and breeding of animals and plants to provide food, fiber, medicinal plants and other products to sustain and enhance life. Agriculture was the key development in the rise of sedentary human civilization, whereby farming of domesticated species created food surpluses that enabled people to live in cities. The study of agriculture is known as agricultural science. The history of agriculture dates back thousands of years; people gathered wild grains at least 105,000 years ago and began to plant them around 11,500 years ago before they became domesticated. Pigs, sheep, and cattle were domesticated over 10,000 years ago. Crops originate from at least 11 regions of the world. Industrial agriculture based on large-scale monoculture has in the past century come to dominate agricultural output, though about 2 billion people worldwide still depend on subsistence agriculture.
  <br>
  Modern agronomy, plant breeding, agrochemicals such as pesticides and fertilizers, and technological developments have sharply increased yields from cultivation, but at the same time have caused widespread ecological and environmental damage. Selective breeding and modern practices in animal husbandry have similarly increased the output of meat, but have raised concerns about animal welfare and environmental damage through contributions to global warming, depletion of aquifers, deforestation, antibiotic resistance, and growth hormones in industrially produced meat. Genetically modified organisms are widely used, although they are banned in several countries.</p>
  <h2></h2>
  <p>Plant diseases have caused severe losses to humans in several ways. Starvation and uprooting of families resulted from the Irish famine caused by potato late blight (caused by Phytophthora infestans). A valued resource was lost with the virtual elimination of the American chestnut by chestnut blight (caused by Cryphonectria parasitica). And direct economic loss such as the estimated one billion dollars lost in one year to American corn growers from southern corn leaf blight (caused by Cochliobolus maydis, anamorph Bipolaris maydis). Many plant diseases cause less dramatic losses annually throughout the world but collectively constitute sizable losses to farmers and can reduce the aesthetic values of landscape plants and home gardens.
  <br>
  The goal of plant disease management is to reduce the economic and aesthetic damage caused by plant diseases. Traditionally, this has been called plant disease control, but current social and environmental values deem “control” as being absolute and the term too rigid. More multifaceted approaches to disease management, and integrated disease management, have resulted from this shift in attitude, however. Single, often severe, measures, such as pesticide applications, soil fumigation or burning are no longer in common use. Further, disease management procedures are frequently determined by disease forecasting or disease modeling rather than on either a calendar or prescription basis. Disease management might be viewed as proactive whereas disease control is reactive, although it is often difficult to distinquish between the two concepts, especially in the application of specific measures.</p>
