<h1>Southern Leaf Blight of Maize</h1>
<h3>Disease: Southern Leaf
Blight of Maize<br>
Pathogen:<br>
Anamorph (asexual phase)<br>
• Bipolaris maydis [Syn.<br>
Helminthosporium maydis]<br>
• Teliomorph (sexual phase)<br>
Cochliobolus heterostrophus<br>
Host: Maize</h3></center>
<img src="images/s_leaf_blight_Maize.jpg" width="400" height="300"style="float : right"/>

<h2>Symptoms
</h2>
<p>There are 3 physiological races
of Bipolaris maydis :</p><center><p>Race T,<br>
Race O, <br>Race C.<br> </center><br><p>Race O
causes long, tan, lesions that
have brown borders on leaves.
Race T and race C are known to
be specifically virulent to corn
with cytoplasm male‐sterile T and
cytoplasm male‐sterile C,
respectively.<br>**Lesions on the leaves caused by Race 0 of the fungus
are elongated tan coloured between the veins up to
one inch long
Lesions produced by Race T (which was very
prevalent in the early 1970s) are tan spindle-shaped or
elliptical, with yellow-green or chlorotic halos. Later,
the Race T lesions often have dark, reddish-brown
borders and may occur on all parts of the plants</p>
<h2>Predisposing Factors</h2>
<p>•Warm, temperatures (70 - 90° F)
and wet conditions<br>
•Monoculture<br>
•Reduced tillage</p>
<h2>Disease management</h2>
<p>Use of resistant hybrids<br>
•Crop rotation with non-host
species<br>
•General sanitation-Destruction of
crop debris.<br>
•Spray Zineb/Maneb @ 0.25% at
10 days intervals</p>

