<h1>Brown spot Of Maize</h1>
<h3>Disease: Brown
spot of Maize<br>
Pathogen:
Physoderma
maydis<br>
Host: Maize</h3></center>
<img src="images/brown_spot_maize.jpg" width="400" height="300"style="float : right"/>

<h2>Symptoms and sign</h2>
<p>Lesions occur mainly on the leaf, but may also occur
on leaf sheath, stalks, outer ear husks and tassels
producing small round-to-oblong a chocolate brown
to reddish brown spots which may merge together to
form large brown blotches. Spots on the mid-ribs are
circular and dark brown</p>
<h2>Predisposing Factors</h2>
<p>Brown spot is favored by<br>
•high temperatures and<br>
•high humidity.</p>
<h2>Disease Cycle</h2>
<p>•Overwintering fungal mycelium and
sporangia in infected maize debris or
soil.<br>
•Sporangia germinate to produce
infective zoospores under conditions
of moisture and light</p>
<h2>Disease management</h2>
<h3>Use of tolerant varieties<br></h3>
<p>
•Crop rotation, as the fungus
survives in infected crop
residue<br>
•Good cultural practices<br>
-Sanitation<br>
--proper spacing</p>
