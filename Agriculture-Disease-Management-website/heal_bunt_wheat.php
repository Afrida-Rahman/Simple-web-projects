<h2>Symptoms and Signs</h2>
<p>Plants with common
bunt may be moderately
stunted but infected
plants cannot be easily
recognized until near
maturity and even then
it is seldom
conspicuous.<br>**
After initial infection, the entire kernel is converted
into a sorus consisting of a dark brown to black mass
of teliospores covered by a modified periderm, which
is thin and papery. The sorus is li
ght to dark brown and
is called a bunt ball.<br>
**The bunted heads are slender,
bluish-green and may stay
greener longer than healthy
heads. The bunt balls change to a
dull gray-brown at maturity, at
which they become conspicuous.
The fragile covering of the bunt
balls are ruptured at harvest,
producing clouds of spores. The
spores have a fishy odor</p>
<h2>Management of Hill Bunt of Wheat</h2>
<h3>Disease control:</h3><p>
The disease is externally seed borne and systemic infection
is there -as such spray of fungicides is not effective in
controlling the disease.<br>
• Seed treatment Systemic fungicides like carboxin, vitavax
and benlate @ 2.0 g/Kg seed and Tilt (propioconazole) 25
EC @ 0.1% are used for seed treatment. A combination of
vitavax with thiram is very effective for disease control.<br>
• Use of resistant varieties:
The most successful method is the use of resistant varieties.<br>
• Crop rotation:
Crop rotation at suitable intervals is also effective in disease
control</p>

