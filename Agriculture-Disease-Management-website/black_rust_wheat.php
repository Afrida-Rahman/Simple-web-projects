<h1>Stem or black rust of wheat</h1>

<h3>DISEASE: Stem rust (black rust or cereal rusts)<br>
PATHOGEN: Puccinia graminis f. sp. tritici<br>
HOSTS: Wheat, barley and common
barberry( Berberis,Mahoberberis,
and Mahonia spp.)</h3><br></center>
<img src="images/black_rust_wheat.jpg" width="400" height="300"style="float : right"/>

<h2>Symptoms and Signs</h2>
<h3>On wheat and other grass hosts:</h3>
<p>
 After 7 to 15 days infection<br>
 Elongated to irregular shaped pustules (uredinia) of brickred
urediniospores break through the epidermis and
develop on the upper leaf surfaces, leaf sheaths, glumes
and awns.</p>
<p>*  Microscopically, these red spores are
covered with fine spines.<br>

*  The pustules may be abundant and produced on
both leaf surfaces and stems of host plants. <br>

*  Later in the season, pustules of black teliospores
(telia) begin to appear in infected host plants.<br>
*  Microscopically, teliospores are two celled and
thick walled
.
<br>
<h2>Pre disposing (environmental) factors</h2>

<p>• Warm ‐humid weather conditions with
intermittent rains.<br>
• Hot days 25‐30ºC and mild nights (15‐20ºC)<br>
• Leaf wetness from rain or dew<br></p>
<h2>Management of rusts of wheat</h2>
<h3>Prevention, early warning and rapid response</h3>
<p> Using resistant cultivars and early intervention are the
key principles of controlling wheat rust diseases.<br>
 Ug99 is a virulent strain of stem rust which emerged
in Uganda in 1998-1999 . Most of the wheat
varieties are susceptible to this strain<br>
 FAO in collaboration with CIMMYT provide policy
and technical support to the concerned countries to
combat this threat in the context of the Borlaug
Global Rust Initiative.<br></p>
<h3>Cultural practices</h3><p>
Grow early maturing resistant wheat varieties.<br>
Appropriate spacing and fertilizer (N)
application
Chemical control
Prophylactic sprays of Mancozeb (Dithane Z‐
78) @ 0.25%<br>
Fungicides that inhibit the synthesis of sterols
[i.e., sterol biosynthesis inhibitors‐SBIs] like
Tilt (Propioconazole) 25 EC @ 0.1% at 15 days
intervals.
</p><br>
