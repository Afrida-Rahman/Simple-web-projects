<h2>Symptoms and Signs</h2>
<p>•  ‘Kresek’ occurs in early stage (Seedling
wilt-Plants withers and dies up).<br>
•  In later stage blightening starts from the
tip of leaves to the base.<br>
•  Straw turned yellow to white.<br>
•  Partially filled grains.<br>
•  Appearance of bacterial ooze (a milky or
opaque dew drop) on young lesions. <br>** The gram negative bacteria causing the disease
are rod-shaped and devoid of capsules. </p>
<h2>Management of Bacterial blight of rice</h2>
<h3>Management Strategy</h3>
<p>• The use of resistant varieties.<br>
• Seed treatment with bleaching powder (100µg/ml) and
zinc sulfate (2%)<br>
• Field sanitation such as removing weed hosts, rice straws,
ratoons and volunteer seedlings.<br>
• Use balanced fertilizer doses, especially nitrogen.<br>
• Proper plant spacing.<br>
• Spray Blitox (Copper oxychloride) 50 W P alternate with
Streptocycline with the appearance of disease</p>

