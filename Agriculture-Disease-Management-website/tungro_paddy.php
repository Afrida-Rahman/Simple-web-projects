<h2>Symptoms and Signs</h2>
<p>Yellow discoloration begins from
leaf tip and extends down to the
blade or the lower leaf portion<br>
• Infected leaves may also show
mottled or striped appearance -
stunting<br>
• Reduced tillering.<br>
• Delayed flowering, which may
delay maturity.<br>
• Most panicles sterile or partially
filled grains and covered with
dark brown blotches.</p>

<p>In field the symptoms of tungro are confused with
the symptoms of nitrogen or zinc deficiency. In case
of zinc and nitrogen deficiency- uniform yellow
discoloration of plants is there. But virus infected
symptoms usually appear in isolated patches</p>

<h2>Predisposing Factors</h2>
<p>• Presence of the virus sources.<br>
• Presence of the vector.<br>
• Age and susceptibility of host plants.<br>
• Synchronization of the three above
factors.<br>
• All growth stages of the rice plant
specifically the vegetative stage</p>
<h2>Management of Tungro disease of
rice</h2>
<p>Management Strategy<br>
• The use of resistant varieties- IR 36, IR 50 ,ADT
37, Ponmani, Co 45, Co 48, Surekha, Vikramarya,
Bharani, IR 36 and white ponni .<br>
• Eradication of tungro hosts-destroy stubbles .<br>
• Elimination of vectors.<br>
- light traps<br>
-spraying Monocrotophos @ 1.6 to 2.2ml/litre of
water<br>
• Crop rotation.</p>
